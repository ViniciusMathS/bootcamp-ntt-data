nome = "Vinicius Matheus de Souza"

print(nome[0]) # retorna a primeira letra

print(nome[-1]) # retorna a ultima letra 

print(nome[:8]) # corta a string a partir do inicio até o numero informado

print(nome[9:]) # corta a string a partir do numero informado até o final 

print(nome[9:16]) # corta a string a partir do numero informado até o segundo numero informado 

print(nome[9:16:2]) # muda o intervalo

print(nome[:]) # pode ser usado para gerar uma cópia

print(nome[::-1]) # inverte a string